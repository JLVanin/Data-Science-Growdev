#12) Escreva um programa que receba a idade de 3 pessoas, e calcule o
#somatório dessas idades.

p_1 = int(input('Idade da primeira pessoa: '))
p_2 = int(input('Idade da segunda pessoa: '))
p_3 = int(input('Idade da tereceira pessoa: '))
total = (p_1 + p_2 + p_3)
print(f'O total das idades é {total} anos')